public class History1 {
	private String id;
	private String dunum;
	private String damout;
	private String date;
	private String charger;
	private String dulimit;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDunum() {
		return dunum;
	}
	public void setDunum(String dunum) {
		this.dunum = dunum;
	}
	public String getDamout() {
		return damout;
	}
	public void setDamout(String damout) {
		this.damout = damout;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getCharger() {
		return charger;
	}
	public void setCharger(String charger) {
		this.charger = charger;
	}
	public String getDulimit() {
		return dulimit;
	}
	public void setDulimit(String dulimit) {
		this.dulimit = dulimit;
	}
	
	
}
